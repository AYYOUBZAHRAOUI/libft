#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, unsigned int n)
{
	unsigned char *x;
	unsigned char *y;
	unsigned int i;
	x = (unsigned char *)s1;
	y = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (x[i] != y[i])
			return (x[i] - y[i]);
		i++;
	}
	return (0);
}
