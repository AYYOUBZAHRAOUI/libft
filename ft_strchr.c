char *ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == (char)c)
			return (char *)s;
		else 
			s++;
	}	
	if ((char)c == '\0')
		return (char *)s;
	else
		return (0);
}